public class ElseIf {
    public static void main(String[] args) {
       int age = 19;
       if(age==18){
           System.out.println("You just been 18");
       } else if (age>18) {
           System.out.println("You can vote");
       }
       else {
           System.out.println("You can not vote");
       }
    }
}